const commonLanguages = [
  'pt', 'it', 'ko', 'tr', 'nl', 'pl', 'sv', 'id', 'vi', 'th', 'he',
  'el', 'cs', 'da', 'fi', 'no', 'hu', 'ro', 'bn', 'pa', 'jv',
  'te', 'mr', 'ta', 'ur', 'gu', 'kn', 'ml', 'uk', 'fa', 'ms',
  'uz', 'az', 'hr', 'sk', 'sl', 'et', 'lv', 'lt', 'sq'
];

commonLanguages.forEach(lang => {
  if (!translations[lang]) {
    translations[lang] = { ...translations.en };
  }
});

export const t = (key: string, lang: AppLanguage = 'en', params?: Record<string, string | number>) => {
  const langKey = lang as string;
  let text = (translations[langKey] && translations[langKey][key]) || (translations.en[key] || key);
  
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v.toString());
    });
  }
  
  return text;
};

export const isRTL = (lang: AppLanguage = 'en'): boolean => {
  return ['ar', 'he', 'fa', 'ur'].includes(lang);
};
